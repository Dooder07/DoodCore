    /**
     * Decode.<br/><br/>
     *
     * Examples:<br/>
     * &amp;szlig; would become &szlig;<br/>
     * &amp;#163; would become &#163;<br/>
     * "Hello, my name is &amp;#198;" would become "Hello, my name is &#198";
     *
     *
     * @param s String to decode
     * @return Decoded String
     */
    public static String decode(String s) {
        for (HtmlEntity e : HtmlEntity.values()) {
            s = s.replace(e.htmlCode, e.value);
            if (e.htmlName != null)
                s = s.replace(e.htmlName, e.value);
        }
        return s;
    }
